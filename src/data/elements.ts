/**
 * Get element by atomic number. Returns undefined if not in table.
 */
export function getElement(atomicNumber: number): ChemicalElement | undefined {
  return elements[atomicNumber];
}

/**
 * Get element by symbol (case-insensitive).
 */
export function getElementBySymbol(
  symbol: string,
): ChemicalElement | undefined {
  const s = symbol.charAt(0).toUpperCase() + symbol.slice(1).toLowerCase();
  return Object.values(elements).find((e) => e.symbol === s);
}

/**
 * All defined element numbers.
 */
export function allElementNumbers(): number[] {
  return Object.keys(elements)
    .map(Number)
    .sort((a, b) => a - b);
}
